package com.stee.spfcore.vo.benefits;

public class DashboardStatus {
	
	private String status;
	
	private int newbornCount;
	
	private int weddingCount;
	
	private int bereavementCount;
	
	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
	
	public int getNewbornCount() {
		return newbornCount;
	}

	public void setNewbornCount(int newbornCount) {
		this.newbornCount = newbornCount;
	}
	
	public int getWeddingCount() {
		return weddingCount;
	}

	public void setWeddingCount(int weddingCount) {
		this.weddingCount = weddingCount;
	}

	public int getBereavementCount() {
		return bereavementCount;
	}

	public void setBereavementCount(int bereavementCount) {
		this.bereavementCount = bereavementCount;
	}

}
